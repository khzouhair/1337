int	ft_sqrt(int nb)
{
	int	i;
	int	k;

	k = 0;
	i = 1;
	while (k < nb)
	{
		k = i * i;
		if (k == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
